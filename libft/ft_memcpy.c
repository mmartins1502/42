#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned int i;

	i = 0;
	while (n--)
	{
		((unsigned char*)dest)[i] = ((unsigned const char*)src)[i];
		i++;
	}
	return (dest);
}
