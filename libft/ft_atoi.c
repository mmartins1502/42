#include "libft.h"

int		ft_atoi(const char *s)
{
	int		i;
	int		res;
	int		neg;

	i = 0;
	neg = 0;
	res = 0;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t' || s[i] == '\v' ||
			s[i] == '\f' || s[i] == '\r')
		i++;
	if (s[i] == '-')
		neg = 1;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		res *= 10;
		res += s[i] - '0';
		i++;
	}
	if (neg)
		return (-res);
	return (res);
}
